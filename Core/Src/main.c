  HAL_GPIO_WritePin(GPIOC, LED_BLUE_Pin|LED_GREEN_Pin|LED_RED_Pin, GPIO_PIN_RESET); //fires external LEDS

  HAL_TIM_Base_Start_IT(&htim6); //idk remmember what this does - some interrupt...hmmmm -

